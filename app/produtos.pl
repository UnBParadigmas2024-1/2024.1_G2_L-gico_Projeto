    write('Digite o nome do mercado: '), nl,
    read(Mercado),
    ( produto(Mercado, _, _) ->
        findall((Produto, Preco), produto(Mercado, Produto, Preco), ListaProdutos),
        imprimir_cor(azul, 'Produtos do mercado '), write(Mercado), nl,
        listar_produtos(ListaProdutos)
    ;
        write('Mercado n√£o encontrado!'), nl
    ).

% Fun√ß√£o auxiliar para imprimir a lista de produtos
listar_produtos([]).
listar_produtos([(Produto, Preco)|T]) :-
    write('- Produto: '), write(Produto), write(', Pre√ßo: '), write(Preco), nl,
    listar_produtos(T).


% Predicado auxiliar para verificar se um produto j√° est√° cadastrado em um mercado
produto_existe(Mercado, Produto) :-
    produto(Mercado, Produto, _).

% Fun√ß√£o para cadastrar produtos
cadastrar_produto :-
    write('Digite o nome do mercado: '), nl,
    read(Mercado),
    write('Digite o nome do produto: '), nl,
    read(Produto),
    ( produto_existe(Mercado, Produto) ->
        write('Produto j√° cadastrado nesse mercado!'), nl
    ;
        write('Digite o pre√ßo do produto: '), nl,
        read(Preco),
        assertz(produto(Mercado, Produto, Preco)),
        write('Produto cadastrado com sucesso!'), nl
    ).